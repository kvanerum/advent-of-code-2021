package day17;

public class Target {
    public int minX;
    public int maxX;
    public int minY;
    public int maxY;

    @Override
    public String toString() {
        return "Target{" +
               "minX=" + minX +
               ", maxX=" + maxX +
               ", minY=" + minY +
               ", maxY=" + maxY +
               '}';
    }
}
